mem = {1: 1, 2: 2, 3: 4}

def solve(n):
    try:
        if not mem.get(n):
            mem[n] = solve(n - 1) + solve(n - 2) + solve(n - 3)
    finally:
        return mem[n]

for _ in range(int(input())):
    n = int(input())
    print(solve(n))


